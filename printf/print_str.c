#include "ft_printf.h"

void	print_str(char *s, int *count)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		(*count)++;
		i++;
	}
}
